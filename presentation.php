                <h2 class="vhmavi-presentation-h2">Une éducation ni gratuite, ni obligatoire</h2>
                <p>Le système éducatif népalais peine encore à former les générations futures.</p>
                <p>Différent du système français, les élèves népalais passent un certificat d'étude, équivalent du baccalauréat, à la fin de la classe 10, âgés seulement de 16 ans. Du fait de leur jeune âge, ils doivent poursuivre 2 ans dans le cycle secondaire supérieur avant d'intégrer l'université.</p>
                <p>Les établissements publics, sous-équipés et disposant d'enseignants peu qualifiés, affichent seulement 30% de réussite au certificat d'étude, qu'ils obtiennent à seulement 16 ans. L'enseignement y est fondé sur le par-cœur et l'autorité, et ne laisse aucune place à l'ouverture d'esprit et aux valeurs sociales.</p>
                <p>En réponse, un grand nombre d'écoles privées, ont vu le jour. Bien que leurs résultats soient bien meilleurs, leurs frais d'inscription parfois exhorbitants ne favorisent pas l'égalité des chances. Enfin, elles mettent en avant le travail expatrié, au dépens de la société népalaise.</p>
